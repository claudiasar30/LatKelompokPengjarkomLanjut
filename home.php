<h1 class="entry-title">SMA N 1 Lubuk Alung</h1>
<p class="enttry-summary" style="display: none;"></p>
<div class="entry-content">
    <p>SMA N 1 Lubuk Alung adalah sebuah sekolah menengah atas yang berlokasi di Kabupaten Padang Pariaman, Provinsi Sumatera Barat, didirikan pada tahun 1979. Sejak pendiriannya, sekolah ini telah berkomitmen untuk menyediakan pendidikan berkualitas tinggi dan membentuk siswa-siswanya menjadi individu yang berprestasi dan bertanggung jawab.</p>
    <p>Visi :</p>
    <p>Menjadi lembaga pendidikan unggul yang menghasilkan generasi penerus bangsa yang cerdas, berkarakter, dan berkontribusi positif bagi masyarakat.</p>
    <p>Misi :</p>
    <p>1. Memberikan pendidikan berkualitas tinggi dengan fasilitas dan kurikulum yang up-to-date.</p>
    <p>2. Mendorong pengembangan karakter dan nilai-nilai moral pada setiap siswa.</p>
    <p>3. Mendukung perkembangan bakat dan minat siswa melalui berbagai kegiatan ekstrakurikuler.</p>
    <p>4. Membangun kemitraan yang kuat antara sekolah, siswa, dan orang tua.</p>
</div>